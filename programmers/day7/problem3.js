function solution(n) {
    let cur = n + 1;
    
    while (true) {
        if(getBinaryOneLength(n) === getBinaryOneLength(cur)) {
            return cur;
        }
        
        cur++;
    }
}

const getBinaryOneLength = (n) => {
    const str = n.toString(2);
    const length = str.match(/1/g).length;

    return length;
} 
function solution(num_list) {
    return num_list.reduce((acc, cur) => acc + countMakeNumberToOne(cur), 0);
}

const countMakeNumberToOne = (n) => {
    let count = 0;
    
    while(n !== 1) {
        if(n % 2 === 0) {
            n = n / 2
        } else {
            n = (n - 1) / 2
        }
        count++
        
        if(n === 1) return count;
    }
    
    return count;
}